package HW_26_January;

public class oneDimensionalArray {
    public static void main(String[] args) {
        int[] arr = {50, 64, 57, 84, 64, 56, 98, 55};

        System.out.print("\nArray Elements: ");

        int n = arr.length;
        for (int i=0; i <n; i++) {
            System.out.print(arr[i]+" ");
        }
        System.out.println();
    }
}
